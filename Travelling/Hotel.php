	function searchHotels() {
		const locationInput = document.getElementById('location');
		const checkInInput = document.getElementById('check-in');
		const checkOutInput = document.getElementById('check-out');
		const hotelList = document.getElementById('hotel-list');

		// Get user input values
		const location = locationInput.value;
		const checkInDate = checkInInput.value;
		const checkOutDate = checkOutInput.value;

		// You can use the Google Places API to search for hotels based on location.
		// This is a simplified example without actual API calls.
		// Replace this with your actual API implementation.
		const hotels = [
			{ name: 'Hotel A', location: 'Location A', checkIn: '2023-09-23', checkOut: '2023-09-25' },
			{ name: 'Hotel B', location: 'Location B', checkIn: '2023-09-24', checkOut: '2023-09-26' },
			{ name: 'Hotel C', location: 'Location C', checkIn: '2023-09-26', checkOut: '2023-09-28' },
		];

		// Clear the previous results
		hotelList.innerHTML = 'clear';

		// Filter hotels based on location and check-in/check-out dates
		const filteredHotels = hotels.filter(hotel => {
			return hotel.location.toLowerCase().includes(location.toLowerCase()) &&
				hotel.checkIn <= checkInDate &&
				hotel.checkOut >= checkOutDate;
		});

		// Display hotel data
		filteredHotels.forEach(hotel => {
			const hotelItem = document.createElement('li');
			hotelItem.textContent = `${hotel.name} - ${hotel.location}`;
			hotelList.appendChild(hotelItem);
		});
	}

	// Attach the search function to the button click event
	const searchButton = document.getElementById('search-button');
	searchButton.addEventListener('click', searchHotels);
